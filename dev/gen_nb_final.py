    "    rng = random.Random(seed)",
    "    genuine_keys = [rng.randint(0, 49) for _ in range(N_GENUINE)]",
    "    genuine_values = [rng.randint(0, 49) for _ in range(N_GENUINE)]",
    "    filler_keys = [rng.randint(0, 49) for _ in range(60)]",
    "    filler_values = [rng.randint(0, 49) for _ in range(60)]",
    "",
    "    input_ids = []",
    "    oracle_actions = []",
    "",
    "    # Encode: 100 steps (50 key+value pairs)",
    "    for i in range(100):",
