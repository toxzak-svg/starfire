    "# === Episode Generation ===",
    "",
    "from dataclasses import dataclass",
    "from typing import List",
    "",
    "N_GENUINE = 20",
    "EPISODE_LENGTH = 170",
    "QUERY_START = 150",
    "IGNORE_INDEX = -100",
