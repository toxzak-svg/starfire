"\n"
"class FrozenEmbedding(nn.Module):\n"
"    def __init__(self, v_total=V_TOTAL, d_embed=64, seed=0):\n"
"        super().__init__()\n"
"        g = torch.Generator().manual_seed(seed)\n"
"        self.register_buffer('weight', F.normalize(torch.randn(v_total, d_embed, generator=g), dim=-1))\n"
"    def forward(self, token_ids): return F.embedding(token_ids, self.weight)\n"
"\n"
"class BudgetEnforcer:\n"
"    def __init__(self, B=20, T_window=100):\n"
"        self.B = B; self.T_window = T_window; self._reset()\n"
"    def _reset(self):\n"
"        self.accesses = []; self.total_permitted = 0; self.blocked = 0; self.requested = 0\n"
"    def step(self, action_idx):\n"
"        self.requested += 1\n"
"        if action_idx == 0: return True\n"
"        window = self.accesses[max(0, len(self.accesses)-self.T_window):]\n"
"        if len(window) >= self.B: self.blocked += 1; return False\n"
"        self.accesses.append(action_idx); self.total_permitted += 1; return True\n"
"    @property\n"
"    def budget_remaining(self):\n"
"        window = self.accesses[max(0, len(self.accesses)-self.T_window):]\n"
"        return max(0, self.B - len(window))\n"
"    @property\n"
"    def budget_remaining_normalised(self): return self.budget_remaining / self.B\n"
"\n"
"class ExternalMemory(nn.Module):\n"
"    def __init__(self, capacity=200, key_dim=64, value_dim=64, write_threshold=0.5):\n"
"        super().__init__()\n"
"        self.capacity = capacity; self.key_dim = key_dim\n"
"        self.value_dim = value_dim; self.write_threshold = write_threshold; self.reset()\n"
"    def reset(self): self.keys = []; self.values = []; self.saliences = []\n"
"    def write(self, key, value, salience=1.0, override_threshold=False):\n"
"        if salience < self.write_threshold and not override_threshold: return\n"
"        if len(self.keys) >= self.capacity:\n"
"            idx = self.saliences.index(min(self.saliences))\n"
"            self.keys.pop(idx); self.values.pop(idx); self.saliences.pop(idx)\n"
"        self.keys.append(key.detach()); self.values.append(value.detach()); self.saliences.append(salience)\n"
"    def read(self, query):\n"
"        if not self.keys: return torch.zeros(self.value_dim, device=query.device), torch.tensor(0.0)\n"
"        keys_t = torch.stack([k.squeeze(0) if k.dim()>1 else k for k in self.keys])\n"
"        scores = F.cosine_similarity(query.unsqueeze(0), keys_t, dim=-1)\n"
"        idx = scores.argmax().item()\n"
"        return self.values[idx].to(query.device), scores[idx]\n"
"    @property\n"
"    def occupancy(self): return len(self.keys)\n"
"\n"
"class ArchitectureB(nn.Module):\n"
"    def __init__(self, d_embed=64, hidden_dim=128, budget_B=20, budget_T=100,\n"
"                 write_threshold=0.5, embed_seed=0, use_sr_moe=True,\n"
"                 n_codes=16, ring_dim=16, n_prefs=32, lambda_div=0.1,\n"
"                 tau_start=1.0, tau_end=0.01, anneal_steps=5000):\n"
"        super().__init__()\n"
"        self.d_embed = d_embed; self.hidden_dim = hidden_dim\n"
"        self.budget_B = budget_B; self.budget_T = budget_T\n"
"        self.write_threshold = write_threshold; self.use_sr_moe = use_sr_moe\n"
"        self.embedding = FrozenEmbedding(V_TOTAL, d_embed, seed=embed_seed)\n"
"        self.gru = nn.GRUCell(d_embed + d_embed, hidden_dim)\n"
"        if use_sr_moe:\n"
"            self.routing = SRMoEHead(hidden_dim, n_codes=n_codes, ring_dim=ring_dim,\n"
"                                     n_prefs=n_prefs, tau_start=tau_start, tau_end=tau_end,\n"
"                                     anneal_steps=anneal_steps, lambda_div=lambda_div)\n"
"        else:\n"
"            self.routing = nn.Sequential(nn.Linear(hidden_dim+4, hidden_dim), nn.ReLU(), nn.Linear(hidden_dim, 3))\n"
"        self.memory = ExternalMemory(200, d_embed, d_embed, write_threshold)\n"
"        self.budget = BudgetEnforcer(budget_B, budget_T)\n"
"        self.output_head = nn.Linear(hidden_dim + d_embed, OUTPUT_VOCAB)\n"
"        self._hidden = None; self._step = 0\n"
"        self._pending_write_key = None; self._prev_emb = None\n"
"        self._primed_oracle_actions = None\n"
"\n"
"    def reset_episode(self):\n"
"        self._hidden = torch.zeros(1, self.hidden_dim, device=next(self.parameters()).device)\n"
"        self._step = 0; self._pending_write_key = None; self._prev_emb = None\n"
"        self.memory.reset(); self.budget._reset()\n"
"        if hasattr(self.routing, 'reset'): self.routing.reset()\n"
"\n"
"    def prime_oracle_schedule(self, actions): self._primed_oracle_actions = actions\n"
"\n"
"    def forward(self, token_id, route_stop_grad=True, ablation_no_mem=False, ablation_rand_routing=False):\n"
"        if self._hidden is None: self.reset_episode()\n"
"        device = token_id.device; t = self._step; self._step += 1\n"
"        is_query_step = (t >= QUERY_START)\n"
"        emb = self.embedding(token_id.view(1) if token_id.dim()==0 else token_id).squeeze(0)\n"
"        budget_norm = self.budget.budget_remaining_normalised\n"
"        _O2I = {'SKIP': 0, 'READ': 1, 'WRITE': 2, 'PERCEIVE': 3}\n"
"        if self._primed_oracle_actions and t < len(self._primed_oracle_actions):\n"
"            action_idx = _O2I.get(self._primed_oracle_actions[t], 0)\n"
"            hfr = self._hidden.detach() if route_stop_grad else self._hidden\n"
"            lr, _, ls = self.routing(hfr, budget_norm, t/EPISODE_LENGTH, float(t%2), float(is_query_step))\n"
"        elif ablation_rand_routing:\n"
"            action_idx = random.randint(0, 2); lr = torch.zeros(3, device=device); ls = 1.0/3\n"
"        else:\n"
"            hfr = self._hidden.detach() if route_stop_grad else self._hidden\n"
"            lr, action_idx, ls = self.routing(hfr, budget_norm, t/EPISODE_LENGTH, float(t%2), float(is_query_step))\n"
"        if action_idx == 3: self._pending_write_key = emb.detach().clone(); action_idx = 0\n"
"        _act_before = action_idx\n"
"        permitted = self.budget.step(action_idx)\n"
"        if not permitted: action_idx = 0\n"
"        retrieved = torch.zeros(self.d_embed, device=device)\n"
"        if action_idx == 1 and not ablation_no_mem:\n"
"            retrieved, _ = self.memory.read(emb); retrieved = retrieved.to(device)\n"
"        elif action_idx == 2 and not ablation_no_mem:\n"
"            wk = self._pending_write_key if self._pending_write_key is not None else (self._prev_emb if self._prev_emb is not None else emb)\n"
"            ws = ls if self._primed_oracle_actions else 1.0\n"
"            self.memory.write(wk, emb, salience=ws); self._pending_write_key = None\n"
"        self._hidden = self.gru(torch.cat([emb, retrieved], dim=-1).unsqueeze(0), self._hidden)\n"
"        self._prev_emb = emb.detach().clone()\n"
"        logits = self.output_head(torch.cat([self._hidden.squeeze(0), retrieved], dim=-1))\n"
"        info = {'step': t, 'action': {0:'SKIP',1:'READ',2:'WRITE'}.get(action_idx,'SKIP'),\n"
"                'action_idx': action_idx, 'blocked': (not permitted) and _act_before in (1,2),\n"
"                'permitted': permitted, 'salience_entropy': ls,\n"
"                'budget_remaining': self.budget.budget_remaining,\n"
"                'occupancy': self.memory.occupancy, 'routing_logits': lr}\n"
